package com.eit.vipo.service.customdto;

import java.util.*;

public class Coordinates {
    private Long height;
    private Long width;
    private Long x;
    private Long y;
    private String hvgColor;

    public Long getHeight() {
        return height;
    }

    public void setHeight(Long value) {
        this.height = value;
    }

    public Long getWidth() {
        return width;
    }

    public void setWidth(Long value) {
        this.width = value;
    }

    public Long getX() {
        return x;
    }

    public void setX(Long value) {
        this.x = value;
    }

    public Long getY() {
        return y;
    }

    public void setY(Long value) {
        this.y = value;
    }

    public String getHvgColor() {
        return hvgColor;
    }

    public void setHvgColor(String value) {
        this.hvgColor = value;
    }
}
